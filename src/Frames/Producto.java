    public void cargar(){
        
         String[] v = new String[2];
        ColaAlimento aux = new ColaAlimento();
       
        while(!cl.esVacia()){  
            Alimento al = cl.elicola();
            
           
            
                v[0] = al.getNombre();
                v[1] = String.valueOf(al.getPrecio());
                modelo.addRow(v);
            
            aux.adicola(al);
                       
        }
        aux.mostrarCola();
        cl.vaciarCola(aux);
    }
    
    
